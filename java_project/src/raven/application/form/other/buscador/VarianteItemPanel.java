    /**
     * Crea una etiqueta de información con ancho fijo
     */
    private JLabel crearEtiquetaInfo(String texto, int ancho) {
        boolean dark = com.formdev.flatlaf.FlatLaf.isLafDark();
        JLabel label = new JLabel(texto);
        label.setPreferredSize(new Dimension(ancho, 20));
        label.setMinimumSize(new Dimension(ancho, 20));
        label.setMaximumSize(new Dimension(ancho, 20));
        label.setForeground(dark ? Color.WHITE : new Color(50, 50, 50));
        return label;
    }
    
    /**
     * Configura estilos FlatLaf
     */
    private void configurarEstilos() {
        putClientProperty(FlatClientProperties.STYLE, "arc: 8");
    }
    
