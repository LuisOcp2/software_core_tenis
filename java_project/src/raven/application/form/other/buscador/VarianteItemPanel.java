    /**
     * Inicializa componentes
     */
    private void initComponents2() {
        setLayout(new BorderLayout(10, 0));
        Color base = UIManager.getColor("Panel.background");
        setBackground(base);
        setBorder(new CompoundBorder(
            new LineBorder(UIManager.getColor("Component.borderColor"), 1, true),
            new EmptyBorder(8, 12, 8, 12)
        ));
        setMaximumSize(new Dimension(Integer.MAX_VALUE, 50));
        
        // Panel de información
        JPanel panelInfo = new JPanel();
        panelInfo.setLayout(new BoxLayout(panelInfo, BoxLayout.X_AXIS));
        panelInfo.setOpaque(false);
        
        // Talla
        JLabel lblTalla = crearEtiquetaInfo("Talla: " + variante.getTalla(), 100);
        panelInfo.add(lblTalla);
        panelInfo.add(Box.createHorizontalStrut(10));
        
        // Color
        JLabel lblColor = crearEtiquetaInfo("Color: " + variante.getColor(), 150);
        panelInfo.add(lblColor);
        panelInfo.add(Box.createHorizontalStrut(10));
        
        int stockDisponible = variante.getStockDisponible() != null ? 
                              variante.getStockDisponible() : 0;
        JLabel lblStock = crearEtiquetaInfo("Stock: " + stockDisponible, 80);
        
        // Colorear según disponibilidad
        if (stockDisponible <= 0) {
            lblStock.setForeground(Color.RED);
        } else if (stockDisponible < 5) {
            lblStock.setForeground(new Color(255, 140, 0)); // Naranja
        } else {
            lblStock.setForeground(new Color(0, 150, 0)); // Verde
        }
        
        panelInfo.add(lblStock);
        panelInfo.add(Box.createHorizontalStrut(10));
        
        int idVar = variante.getIdVariante() != null ? variante.getIdVariante() : 0;
        JLabel lblIdVar = crearEtiquetaInfo("ID Var: " + idVar, 90);
        panelInfo.add(lblIdVar);
        panelInfo.add(Box.createHorizontalStrut(10));

        String tipo;
        Integer stockCaja = variante.getStockCaja();
        Integer stockPares = variante.getStockPares();
        if (stockCaja != null && stockCaja > 0) {
            tipo = "Caja";
        } else if (stockPares != null && stockPares > 0) {
            tipo = "Par";
        } else {
            tipo = "Sin stock";
        }
        JLabel lblTipo = crearEtiquetaInfo("Tipo: " + tipo, 90);
        panelInfo.add(lblTipo);
        panelInfo.add(Box.createHorizontalStrut(10));

        // Precio
        if (variante.getPrecioVenta() != null) {
            JLabel lblPrecio = crearEtiquetaInfo(
                String.format("$%.2f", variante.getPrecioVenta()), 
                100
            );
            lblPrecio.setFont(lblPrecio.getFont().deriveFont(Font.BOLD));
            lblPrecio.setForeground(com.formdev.flatlaf.FlatLaf.isLafDark() ? Color.WHITE : new Color(40, 40, 40));
            panelInfo.add(lblPrecio);
        }
        
        add(panelInfo, BorderLayout.CENTER);
        
        // Botón de selección
        JButton btnSeleccionar = new JButton("Seleccionar");
        btnSeleccionar.setEnabled(stockDisponible > 0);
        btnSeleccionar.putClientProperty(FlatClientProperties.STYLE,
            "arc:18;background:#2D89EF;foreground:#ffffff;borderWidth:0;focusWidth:1");
        btnSeleccionar.addActionListener(e -> {
            if (seleccionListener != null) {
                seleccionListener.run();
            }
        });
        
        add(btnSeleccionar, BorderLayout.EAST);
        
        // Efecto hover
        addMouseListener(new MouseAdapter() {
            @Override
            public void mouseEntered(MouseEvent e) {
                if (stockDisponible > 0) {
                    setBackground(adjustColor(UIManager.getColor("Panel.background"), 0.06f));
                    setBorder(new CompoundBorder(
                        new LineBorder(UIManager.getColor("Component.borderColor"), 1, true),
                        new EmptyBorder(8, 12, 8, 12)
                    ));
                }
            }
            
            @Override
            public void mouseExited(MouseEvent e) {
                setBackground(UIManager.getColor("Panel.background"));
                setBorder(new CompoundBorder(
                    new LineBorder(UIManager.getColor("Component.borderColor"), 1, true),
                    new EmptyBorder(8, 12, 8, 12)
                ));
            }
        });
    }

    private Color adjustColor(Color base, float ratio) {
        boolean dark = com.formdev.flatlaf.FlatLaf.isLafDark();
        Color mix = dark ? Color.WHITE : Color.BLACK;
        int r = (int) (base.getRed() * (1 - ratio) + mix.getRed() * ratio);
        int g = (int) (base.getGreen() * (1 - ratio) + mix.getGreen() * ratio);
        int b = (int) (base.getBlue() * (1 - ratio) + mix.getBlue() * ratio);
        return new Color(r, g, b, base.getAlpha());
    }
