    private List<DataSearch> search(String search) {
        int limitData = 7;
        List<DataSearch> list = new ArrayList<>();
        String sql = "SELECT COALESCE(pv.ean, p.codigo_modelo) AS codigo_barras, p.id_producto, p.nombre, " +
                    "COALESCE(c.nombre, '') AS color, COALESCE(t.numero, '') AS talla " +
                    "FROM productos p " +
                    "LEFT JOIN producto_variantes pv ON p.id_producto = pv.id_producto " +
                    "LEFT JOIN colores c ON pv.id_color = c.id_color " +
                    "LEFT JOIN tallas t ON pv.id_talla = t.id_talla " +
                    "WHERE p.id_producto LIKE ? OR p.nombre LIKE ? OR c.nombre LIKE ? OR t.numero LIKE ? " +
                    "LIMIT ?";

        String url = "jdbc:mysql://" + conexion.host + ":" + conexion.port + "/" + conexion.database;
        try (Connection conn = DriverManager.getConnection(url, conexion.username, conexion.password); PreparedStatement stmt = conn.prepareStatement(sql)) {

            String searchParam = "%" + search + "%";
            stmt.setString(1, searchParam);
            stmt.setString(2, searchParam);
            stmt.setString(3, searchParam);
            stmt.setString(4, searchParam);
            stmt.setInt(5, limitData);

            ResultSet rs = stmt.executeQuery();

            while (rs.next()) {
                String idpString = rs.getString("id_producto");
                String nombre = rs.getString("nombre");
                String color = rs.getString("color");
                String talla = rs.getString("talla");
                String codigoBarras = rs.getString("codigo_barras");

                boolean startsWithSearch = idpString.toLowerCase().startsWith(search.toLowerCase());
                DataSearch data = new DataSearch(idpString, codigoBarras, nombre, color, talla, startsWithSearch);

                if (startsWithSearch) {
                    list.add(0, data); // Prioriza los que empiezan con el texto
                } else {
                    list.add(data);
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return list;
    }

