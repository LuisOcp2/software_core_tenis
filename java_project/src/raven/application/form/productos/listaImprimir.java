    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Recorremos la tabla y recopilamos los productos seleccionados
        for (int i = 0; i < tablaProd.getRowCount(); i++) {
            Boolean seleccionado = (Boolean) tablaProd.getValueAt(i, 0);
            if (seleccionado != null && seleccionado) {
                try {
                    int cantidad = Integer.parseInt(tablaProd.getValueAt(i, 5).toString());

                    // Creamos una nueva tabla temporal con solo esa fila
                    JTable tablaTemporal = new JTable(1, tablaProd.getColumnCount());
                    for (int j = 0; j < tablaProd.getColumnCount(); j++) {
                        tablaTemporal.setValueAt(tablaProd.getValueAt(i, j), 0, j);
                    }

                    // Instanciamos el impresor solo para esa fila
                    ImpresorTermica impresor = new ImpresorTermica(tablaTemporal, cantidad);
                    impresor.imprimir();

                } catch (Exception ex) {
                    ex.printStackTrace();
                    JOptionPane.showMessageDialog(null, "Error al imprimir fila " + i + ": " + ex.getMessage());
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

